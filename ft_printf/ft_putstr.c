#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	i = ft_strlen(s);
	while (*s)
	{
		write(1, s, 1);
		s++;
	}
	return (i);
}
